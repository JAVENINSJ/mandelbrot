class ima{
    double real=0,img=0;    
    public ima(double x,double y){
        this.real=x;
        this.img=y;
    }        
    public ima square(){
        ima sum=new ima(0,0);
        sum.real=this.real*this.real-this.img*this.img;
        sum.img=2*this.real*this.img;
        return sum;
    }
}

