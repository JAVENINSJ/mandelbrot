    private ima iteration(ima a,ima x,int n){
        if(realval.contains(a.real)&&imgval.get(realval.indexOf(a.real))==a.img){ 
            realval.clear();
            imgval.clear();
            return new ima(n,0);            
        }else if(n<k){
            realval.add(a.real);
            imgval.add(a.img);
            n++;
            return iteration(sum(a.square(),x),x,n);           
        }      
        realval.clear();
        imgval.clear();        
        return new ima(-1,0);
    }
    
    public ima sum(ima x,ima y){
        ima sum=new ima(0,0);
        sum.real=x.real+y.real;
        sum.img=x.img+y.img;
        return sum;
    }
    
